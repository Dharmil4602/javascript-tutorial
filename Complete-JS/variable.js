/* Variables Can Be Created With Following Keywords:
1. var
2. let
3. const
*/

var city = `Delhi`;
console.log(city);

{
    let city = `Baroda`;
    city = `Ahmedabad`;
    console.log(city);
}
city = `Surat`;
console.log(city);


const arr = [1, 2, 3, 5];
arr.push(10); // .push is used to add element in the existing array.
console.log(arr);

